package modules.util;

public class PosterPoint {
    public int x;
    public int y;
    public int z;

    public PosterPoint() {}

    public PosterPoint(int x, int y, int z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public void setLocation(int x, int y, int z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }


}
